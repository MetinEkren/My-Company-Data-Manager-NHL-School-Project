DROP DATABASE IF EXISTS Webwinkel;
CREATE DATABASE Webwinkel;

USE Webwinkel;

CREATE TABLE Categorieen
(
    CategorieID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    CategorieNaam VARCHAR(255) NOT NULL,
    Omschrijving VARCHAR(255) NOT NULL
);
CREATE INDEX idx_categorienaam ON Categorieen(CategorieNaam);

CREATE TABLE Leveranciers
(
    LeverancierID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    LeverancierNaam VARCHAR(255) NOT NULL,
    ContactPersoon VARCHAR(255) NOT NULL,
    Adres VARCHAR(255) NOT NULL,
    Stad VARCHAR(255) NOT NULL,
    Postcode VARCHAR(255) NOT NULL,
    Land VARCHAR(255) NOT NULL,
    Telefoon VARCHAR(255) NULL
);
CREATE INDEX idx_leveranciernaam ON Leveranciers(LeverancierNaam);

CREATE TABLE Medewerkers
(
    MedewerkerID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    Achternaam VARCHAR(255) NOT NULL,
    Voornaam VARCHAR(255) NOT NULL,
    Geboortedatum DATE NOT NULL,
    Notities TEXT NULL,
    Foto VARCHAR(50) NULL
);

CREATE TABLE Klanten
(
    KlantID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    KlantNaam VARCHAR(255) NOT NULL, 
    ContactPersoon VARCHAR(255) NOT NULL, 
    Adres VARCHAR(255) NOT NULL, 
    Stad VARCHAR(255) NOT NULL, 
    Postcode VARCHAR(50) NOT NULL, 
    Land VARCHAR(255) NOT NULL
);

CREATE TABLE Verzenddiensten
(
    VerzenddienstID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    Naam VARCHAR(255) NOT NULL, 
    Telefoon VARCHAR(255) NOT NULL
);
CREATE INDEX idx_naam ON Verzenddiensten(Naam);

CREATE TABLE Producten
(
    ProductID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    ProductNaam VARCHAR(255) NOT NULL, 
    LeverancierID INT(11) NOT NULL, 
    CategorieID INT(11) NOT NULL, 
    Eenheid VARCHAR(255) NOT NULL, 
    Prijs FLOAT NOT NULL
);

ALTER TABLE Producten
    ADD FOREIGN KEY (LeverancierID) REFERENCES Leveranciers(LeverancierID);
ALTER TABLE Producten
    ADD FOREIGN KEY (CategorieID) REFERENCES Categorieen(CategorieID);

CREATE TABLE Bestellingen
(
    BestellingID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    KlantID INT(11) NOT NULL, 
    MedewerkerID INT(11) NOT NULL, 
    BestelDatum DATE NOT NULL, 
    VerzenddienstID INT(11) NOT NULL
);

ALTER TABLE Bestellingen
    ADD FOREIGN KEY (KlantID) REFERENCES Klanten(KlantID);
ALTER TABLE Bestellingen
    ADD FOREIGN KEY (MedewerkerID) REFERENCES Medewerkers(MedewerkerID);
ALTER TABLE Bestellingen
    ADD FOREIGN KEY (VerzenddienstID) REFERENCES Verzenddiensten(VerzenddienstID);

CREATE TABLE Bestelregels
(
    BestelregelID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    BestellingID INT(11) NOT NULL, 
    ProductID INT(11) NOT NULL,
    Aantal INT(11) NOT NULL
);

ALTER TABLE Bestelregels
    ADD FOREIGN KEY (BestellingID) REFERENCES Bestellingen(BestellingID);
ALTER TABLE Bestelregels
    ADD FOREIGN KEY (ProductID) REFERENCES Producten(ProductID);

